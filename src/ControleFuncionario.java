    private void navegacaoInterfaceDpto(){
        switch (navegacaoDpto) {
            case NAVEGACAO_NAV:
                btnDptoNovo.setEnabled(true);
                btnDptoEditar.setEnabled(false);
                btnDptoExcluir.setEnabled(false);
                btnDptoSalvar.setEnabled(false);
                btnDptoCancelar.setEnabled(false);
                setEnabledDepartamento(false);
                cleanDepartamento();
                break;
            case NAVEGACAO_NOVO:
                btnDptoNovo.setEnabled(false);
                btnDptoEditar.setEnabled(false);
                btnDptoExcluir.setEnabled(false);
                btnDptoSalvar.setEnabled(true);
                btnDptoCancelar.setEnabled(true);
                setEnabledDepartamento(true);
                cleanDepartamento();
                break;
            case NAVEGACAO_EDITAR:
                btnDptoNovo.setEnabled(true);
                btnDptoEditar.setEnabled(false);
                btnDptoExcluir.setEnabled(false);
                btnDptoSalvar.setEnabled(true);
                btnDptoCancelar.setEnabled(true);
                setEnabledDepartamento(true);
                textDptoCodigo.setEnabled(false);
                break;
            case NAVEGACAO_EXCLUIR:
                btnDptoNovo.setEnabled(true);
                btnDptoEditar.setEnabled(false);
                btnDptoExcluir.setEnabled(false);
                btnDptoSalvar.setEnabled(false);
                btnDptoCancelar.setEnabled(false);
                setEnabledDepartamento(false);
                cleanDepartamento();
                break;
            case NAVEGACAO_SELECAO:
                btnDptoNovo.setEnabled(true);
                btnDptoEditar.setEnabled(true);
                btnDptoExcluir.setEnabled(true);
                btnDptoSalvar.setEnabled(false);                
                btnDptoCancelar.setEnabled(false);
                setEnabledDepartamento(false);
                break;
            default:
                JOptionPane.showMessageDialog(rootPane, "ERRO: Modo de Navegação Inválido na Interface do Departamento!");
                break;
        }
    }
    

    private void setEnabledDepartamento(boolean enabled){
        textDptoCodigo.setEnabled(enabled);
        textDptoNome.setEnabled(enabled);
    }
    
    private void cleanDepartamento(){
        textDptoCodigo.setText("");
        textDptoNome.setText("");
    }

    private void btnDptoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDptoExcluirActionPerformed
        int option = JOptionPane.showConfirmDialog(rootPane, "Confirmar a exclusão?","Sim ou Não", JOptionPane.YES_NO_OPTION); //.showConfirmDialog(rootPane, evt);
        if(option == JOptionPane.YES_OPTION){
            int index = tabDpto.getSelectedRow();
            if (index >= 0 &&  index < tabDpto.getRowCount()){
                int codigoDepartamento = Integer.parseInt(tabDpto.getModel().getValueAt(index, 0).toString());
                boolean excluir = dao.excluirDepartamento(codigoDepartamento);
                if(excluir == true){
                    ((DefaultTableModel)tabDpto.getModel()).removeRow(index);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "ERRO: Exclusão não efetuada.");
                }            
            }
        }
        navegacaoDpto = NAVEGACAO_NAV;
        navegacaoInterfaceDpto();
    }//GEN-LAST:event_btnDptoExcluirActionPerformed

    private void btnDptoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDptoSalvarActionPerformed
        int codigo = Integer.parseInt(textDptoCodigo.getText());
        Departamento departamento = new Departamento(codigo, textDptoNome.getText());        
        if (navegacaoDpto == NAVEGACAO_NOVO) {
            boolean inserir = dao.inserirDepartamento(departamento);
            if(inserir == true){
                DefaultTableModel modelo = (DefaultTableModel) tabDpto.getModel();
                Object linha[] = new Object[] {departamento.getCodigo(), departamento.getNome()};
                modelo.addRow(linha);
            } else {
                JOptionPane.showMessageDialog(rootPane, "ERRO: Inserção não efetuada.");
            }
        } else if (navegacaoDpto == NAVEGACAO_EDITAR) {
            boolean update = dao.atualizarDepartamento(departamento);
            if(update == true){
                int index = tabDpto.getSelectedRow();
                DefaultTableModel modelo = (DefaultTableModel) tabDpto.getModel();
                modelo.setValueAt(departamento.getCodigo(), index, 0);
                modelo.setValueAt(departamento.getNome(), index, 1);            
            } else {
                JOptionPane.showMessageDialog(rootPane, "ERRO: Atualização não efetuada.");                
            }
        }
        navegacaoDpto = NAVEGACAO_NAV;
        navegacaoInterfaceDpto();
    }//GEN-LAST:event_btnDptoSalvarActionPerformed


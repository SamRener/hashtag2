    private void tabDptoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabDptoMouseClicked
        int index = tabDpto.getSelectedRow();
        if (index >= 0 &&  index < tabDpto.getRowCount()){
            int codigoDepartamento = Integer.parseInt(tabDpto.getModel().getValueAt(index, 0).toString());
            Departamento departamento = dao.getDepartamento(codigoDepartamento);
            textDptoCodigo.setText(String.valueOf(departamento.getCodigo()));
            textDptoNome.setText(departamento.getNome());
            navegacaoDpto = NAVEGACAO_SELECAO;
            navegacaoInterfaceDpto();
        }
    }//GEN-LAST:event_tabDptoMouseClicked

        
